// Language manager
class I18n {
  constructor() {
    this.currentLang = this.detectLanguage();
    this.loadLanguage(this.currentLang);
  }
  
  detectLanguage() {
    // Check localStorage first
    const saved = localStorage.getItem('yorozuya-lang');
    if (saved && translations[saved]) {
      return saved;
    }
    
    // Check browser language
    const browserLang = navigator.language || navigator.userLanguage;
    if (browserLang.startsWith('zh')) {
      if (browserLang.includes('TW') || browserLang.includes('HK')) {
        return 'zh-TW';
      }
      return 'zh-CN';
    } else if (browserLang.startsWith('ja')) {
      return 'ja';
    } else if (browserLang.startsWith('en')) {
      return 'en';
    }
    
    // Default to Simplified Chinese
    return 'zh-CN';
  }
  
  loadLanguage(lang) {
    if (!translations[lang]) {
      lang = 'zh-CN';
    }
    
    this.currentLang = lang;
    localStorage.setItem('yorozuya-lang', lang);
    
    // Update html lang attribute
    document.documentElement.lang = lang;
    
    // Translate all elements with data-i18n attribute
    this.translatePage();
    
    // Update language selector
    this.updateLanguageSelector();
  }
  
  translatePage() {
    // Update elements with data-i18n or data-i18n-html attribute
    const elements = document.querySelectorAll('[data-i18n], [data-i18n-html]');
    elements.forEach(el => {
      // Get key from either data-i18n or data-i18n-html attribute
      const key = el.getAttribute('data-i18n') || el.getAttribute('data-i18n-html');
      const translation = this.t(key);
      
      if (translation) {
        // Check if element should use innerHTML (for HTML content like <br>, <strong>)
        if (el.hasAttribute('data-i18n-html')) {
          el.innerHTML = translation;
        } else {
          el.textContent = translation;
        }
      }
    });
    
    // Update placeholders
    const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
    placeholders.forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const translation = this.t(key);
      if (translation) {
        el.placeholder = translation;
      }
    });
    
    // Update aria-labels
    const ariaLabels = document.querySelectorAll('[data-i18n-aria]');
    ariaLabels.forEach(el => {
      const key = el.getAttribute('data-i18n-aria');
      const translation = this.t(key);
      if (translation) {
        el.setAttribute('aria-label', translation);
      }
    });
  }
  
  t(key) {
    return translations[this.currentLang]?.[key] || translations['zh-CN']?.[key] || key;
  }
  
  switchLanguage(lang) {
    this.loadLanguage(lang);
  }
  
  updateLanguageSelector() {
    const selector = document.querySelector('.lang-selector');
    if (selector) {
      const buttons = selector.querySelectorAll('[data-lang]');
      buttons.forEach(btn => {
        const btnLang = btn.getAttribute('data-lang');
        if (btnLang === this.currentLang) {
          btn.classList.add('active');
        } else {
          btn.classList.remove('active');
        }
      });
    }
  }
}

// Initialize when DOM is ready
let i18n;
document.addEventListener('DOMContentLoaded', () => {
  i18n = new I18n();
  
  // Set up language selector event listeners
  const langButtons = document.querySelectorAll('[data-lang]');
  langButtons.forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      const lang = btn.getAttribute('data-lang');
      i18n.switchLanguage(lang);
    });
  });
});
